/* Es un tipo object que permite almacenar varios valores de diferentes tipos */
let numbers = [5, 10, 24, 75];
console.log(numbers);

/* Traverse elements */
console.log(numbers[0])
console.log(numbers[1])
console.log(numbers[2])
console.log(numbers[3])
/* Traverse elements with for */
for(let i = 0; i < numbers.length; i++) {
    console.log(numbers[i]);
}
